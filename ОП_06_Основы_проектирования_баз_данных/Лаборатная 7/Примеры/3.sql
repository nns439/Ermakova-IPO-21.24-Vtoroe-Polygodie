SELECT  
	 	   Название 
      ,Столица 
      ,Площадь 
      ,Население 
      ,Континент 
FROM  
	 	Страны 
WHERE 
	 	Континент = 'Европа' AND 
	 	CAST(Население AS FLOAT) / Площадь > 400 
UNION  
SELECT  
	 	   Название 
