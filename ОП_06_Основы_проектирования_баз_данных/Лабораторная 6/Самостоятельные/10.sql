SELECT 
    д.[Название],
    с.[Фамилия],
    с.[Должность],
    п.[Степень],
    э.[Дата],
    э.[Аудитория]
FROM [Экзамен] э
JOIN [Дисциплина] д ON э.[Код] = д.[Код]
JOIN [Сотрудник] с ON э.[Таб_номер] = с.[Таб_номер]
JOIN [Преподаватель] п ON с.[Таб_номер] = п.[Таб_номер]
WHERE э.[Дата] BETWEEN '2015-06-01' AND '2015-06-30'
ORDER BY э.[Дата];